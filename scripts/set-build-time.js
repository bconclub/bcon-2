const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

/**
 * Build script to capture git metadata at build time
 * Filters out auto-increment commits to show actual meaningful commits
 */

function getGitInfo() {
  try {
    // Get last commit that isn't auto-increment or reset
    const commitMessage = execSync(
      'git log --grep="chore: auto-increment\\|chore: reset" --invert-grep -1 --pretty=format:"%s"',
      { encoding: 'utf-8', stdio: ['ignore', 'pipe', 'ignore'] }
    ).trim();

    // Get commit date
    const commitDate = execSync(
      'git log -1 --pretty=format:"%ci"',
      { encoding: 'utf-8', stdio: ['ignore', 'pipe', 'ignore'] }
    ).trim();

    // Get commit hash
    const commitHash = execSync(
      'git rev-parse --short HEAD',
      { encoding: 'utf-8', stdio: ['ignore', 'pipe', 'ignore'] }
    ).trim();

    // Get branch
    const branch = execSync(
      'git branch --show-current',
      { encoding: 'utf-8', stdio: ['ignore', 'pipe', 'ignore'] }
    ).trim();

    // Get version from package.json
    const packageJson = JSON.parse(fs.readFileSync(path.join(__dirname, '..', 'package.json'), 'utf-8'));
    const version = `v${packageJson.version}`;

    return {
      version,
      commitHash,
      commitMessage: commitMessage || 'No commit message available',
      commitDate: commitDate || new Date().toISOString(),
      branch: branch || 'unknown',
      releaseDate: commitDate ? new Date(commitDate).toISOString() : new Date().toISOString(),
    };
  } catch (error) {
    console.error('Error getting git info:', error.message);
    return {
      version: 'v1.00',
      commitHash: 'unknown',
      commitMessage: 'Git info not available',
      commitDate: new Date().toISOString(),
      branch: 'unknown',
      releaseDate: new Date().toISOString(),
    };
  }
}

function writeEnvFile(gitInfo) {
  const envContent = `# Build-time environment variables
# Auto-generated by scripts/set-build-time.js
# Do not edit manually - regenerated on each build
NEXT_PUBLIC_VERSION=${gitInfo.version}
NEXT_PUBLIC_GIT_COMMIT_HASH=${gitInfo.commitHash}
NEXT_PUBLIC_GIT_COMMIT_MESSAGE=${gitInfo.commitMessage}
NEXT_PUBLIC_GIT_BRANCH=${gitInfo.branch}
NEXT_PUBLIC_COMMIT_DATE=${gitInfo.commitDate}
NEXT_PUBLIC_RELEASE_DATE=${gitInfo.releaseDate}
`;

  const envPath = path.join(__dirname, '..', '.env.production');
  fs.writeFileSync(envPath, envContent);
  console.log('✓ Build info written to .env.production');
  console.log(`  Version: ${gitInfo.version}`);
  console.log(`  Commit: ${gitInfo.commitHash}`);
  console.log(`  Message: ${gitInfo.commitMessage}`);
  console.log(`  Date: ${gitInfo.releaseDate}`);
}

// Main
const gitInfo = getGitInfo();
writeEnvFile(gitInfo);
